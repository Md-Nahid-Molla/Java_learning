
package javaapplication;

public class Assignment_oper {
    public static void main(String[] args) {
        
        int x = 4,y = 2;
        
        x+=y;
        System.out.println("x += "+x);
        
        x-=y;
        System.out.println("x -= "+x);
        
        x*=y;
        System.out.println("x *= "+x);
        
        x/=y;
        System.out.println("x /= "+x);
        
        x%=y;
        System.out.println("x %= "+x);
                
    }
    
}
